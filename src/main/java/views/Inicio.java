    private void btn_BVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_BVehiculoActionPerformed

        /**
         * Metodo que nos permite buscar un vehiculo por medio del id para asi
         * mostrarlo en una tabla, invocando el metodo de findById del
         * VehiculoDaoJdbc.
         */
        Integer id = Integer.parseInt(txt_id.getText());
        VehiculoDaoJdbc buscar = new VehiculoDaoJdbc();
        Vehiculo vehiculo = buscar.findByID(id);
        DefaultTableModel venta = (DefaultTableModel) frm_renta.getModel();

        Object[] row = {vehiculo.getId(), vehiculo.getMarca(), vehiculo.getModelo(),
            vehiculo.getColor(), vehiculo.getPrecio(), vehiculo.getNumeroSerie()};

        venta.addRow(row);

        /**
         * Se hace el calculo del total a pagar multiplicando el numero de dias
         * de la renta por el precio del vehiculo.
         */
        Integer dias = (Integer) cmbDiasRenta.getSelectedItem();
        System.out.println("" + dias);
        Float total = Float.parseFloat(lbl_totalVenta.getText());
        lbl_totalVenta.setText(String.format("%.2f", (vehiculo.getPrecio() * dias) + total));

        texto = lbl_totalVenta.getText();
        java.util.Date ahora = new java.util.Date();

    }//GEN-LAST:event_btn_BVehiculoActionPerformed

